#include "main.h"
/**
 * _isupper - Entry point in program
 * c - parameter allowed
 * checks if character inputed is upperCase
 * _isupper : function already declared that checks if upper or else
 * Return: 1 Success / 0 failed
 */
int _isupper(int c)
{
	if (_isupper(c))
	{
		return (1);
	}
	else
	{
		return (0);
	}
	return (0);
}
